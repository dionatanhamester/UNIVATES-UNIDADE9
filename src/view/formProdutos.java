    private void BotaoExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotaoExcluirActionPerformed
                        
        if (mensagem.pedirConfirmacao(jframe_inicial,"Você deseja mesmo excluir o registro?") == true){       
            try{        
                produtosDAO.Delete(produto);
                produto = null;
                
                limpaTela();
                arrumaBotoes(_DEFAULT);               
            } catch (Exception erro){
                mensagem.exibirMensagem(jframe_inicial,"Erro ao excluir o produto.Verifique" + erro.getMessage(),'e');                
            }
        }
    }//GEN-LAST:event_BotaoExcluirActionPerformed

    private void gridConsultaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_gridConsultaMouseClicked
        if (evt.getClickCount() == 2){
            int row = gridConsulta.getSelectedRow();

            if (row < 0){
                return;
            } else{
                               
                try {
                     String sql = "FROM Produtos where prcodigo = " + gridConsulta.getValueAt(row, 0).toString(); //Posiciona certo no registro que o usuario clicou
                     List<Produtos> listProdutos = produtosDAO.consultaSQL(sql);
                     
                     if (listProdutos.size() > 0){   
                        this.produto = listProdutos.get(0);
                        arrumaBotoes(_DEFAULT);
                        Tab_Selecao.setSelectedIndex(1);                        
                        this.setData();        
                    } else {
                        //como nao tem nada
                    }
                } catch (Exception ex) {
                    System.out.println(ex.getMessage());                    
                }
            }
        }        
    }//GEN-LAST:event_gridConsultaMouseClicked


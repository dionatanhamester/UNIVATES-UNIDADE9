    private void BotaoGravaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotaoGravaActionPerformed
        if (! validarCampos() ){
            mensagem.exibirMensagem(jframe_inicial,"Todos os campos obrigatórios devem ser preenchidos. Verifique!",'i');
        } else { //Segue com o salvar dados.            
            try {               
               salvarDados();
                
               mensagem.exibirMensagem(jframe_inicial,"Registro gravado com sucesso",'i');
                   
               arrumaBotoes(_DEFAULT);
               BotaoNovo.grabFocus();
            } catch (Exception e) {

                System.out.println(e.getMessage());

                mensagem.exibirMensagem(jframe_inicial, "Não foi possível gravar as informações. Tente novamente mais tarde!", 'e');
            }
            
        }
    }//GEN-LAST:event_BotaoGravaActionPerformed

    /*
    Responsável por Salvar dos dados inseridos ou alterados pelo Usuário
    */
    private void salvarDados() throws Exception{             
        produto.setPrnome(Ed_Nome.getText().trim());                
        produto.setPrunidade(Ed_Unidade.getText().trim());
                
        //produto.setGrupos(); //gruposCadastrados.get(cbGrupos.getSelectedIndex())
        //HibernateUtil.getSessionFactory().openSession().load(Grupos.class, gruposCadastrados.get(cbGrupos.getSelectedIndex()))
       
        System.out.print(produto.getGrupos().getGrnome());
        
        if (Check_Inativo.isSelected()){
            produto.setPrinativo("S");
        } else {
            produto.setPrinativo("N");
        }
        
        produto.setPrdataatu(new Date());
        produto.setPrhoraatu(new Date());
        
        if (produto.getId()== null){
            ProdutosId prodID = new ProdutosId(Main.empresaSelecionada.getEmcodigo(), produtosDAO.getAutoIncrement());
            produto.setId(prodID);
            
            produtosDAO.Insert(produto);
        } else {
            produtosDAO.Update(produto);
        }
    }
       
    
